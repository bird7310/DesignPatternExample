package com.ahacool.designpattern.builder.model;

/**
 * @ClassName Roboter
 * @Description A common product.
 * @author Moto
 * @date 2014 2014-5-20
 * 
 */
public class Roboter {
	private String mHead;
	private String mArm;
	private String mBody;
	private String mFoot;

	public String getmHead() {
		return mHead;
	}

	public void setmHead(String mHead) {
		this.mHead = mHead;
	}

	public String getmArm() {
		return mArm;
	}

	public void setmArm(String mArm) {
		this.mArm = mArm;
	}

	public String getmBody() {
		return mBody;
	}

	public void setmBody(String mBody) {
		this.mBody = mBody;
	}

	public String getmFoot() {
		return mFoot;
	}

	public void setmFoot(String mFoot) {
		this.mFoot = mFoot;
	}

}
